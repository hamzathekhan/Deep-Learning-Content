### Here is mathmatical formula for minmax scaler


### (Actual_value - Min_value ) / (Max_value - Min_value)


Actual_value = 40
Min_value = 10
Max_value = 50

MinMaxScaler = (Actual_value - Min_value ) /( Max_value - Min_value)

#### e.g mms = (40-10)/(50-10) #### anwer will be in range of 0 to 1.
